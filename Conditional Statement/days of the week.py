
print("Please Enter the day of the week")
day_of_the_week = input()

if(day_of_the_week == "Sunday"):
    print("Go to Church")

elif(day_of_the_week == "Monday"):
    print("Go to work -Lazy Nigeria youth")

elif(day_of_the_week == "Tuesday"):
    print("It's 5:00am, prepare to go to work")

elif(day_of_the_week == "Wednesday"):
    print("GO and have some Coffee")

elif(day_of_the_week == "Thursday"):
    print("It is Champions League time")

elif(day_of_the_week == "Friday"):
    print("Let's Go Clubbing")

elif(day_of_the_week == "Saturday"):
    print("time to rest")

else:
    print("Invalid Input - (Monday - Sunday accepted")